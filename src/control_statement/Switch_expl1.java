package control_statement;

public class Switch_expl1 {

	public static void main(String[] args)
	{
		
		// 10,20,30,40
     int number=4;//declaration
     
     switch(number)//assignment
     
     {
     case 1:System.out.println(10);
     break;
     case 2:System.out.println(20);
     break;
     case 3:System.out.println(30);
     break;
     case 4:System.out.println(40);
     break;
     default:System.out.println("not in 10, 20,30,40");
     break;
     }
     

	}

}
